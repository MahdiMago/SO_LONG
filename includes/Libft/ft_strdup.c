#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	int		i;
	char	*copy;

	len = ft_strlen(s);
	copy = (char *)malloc(sizeof(char ) * (len + 1));
	if (!copy)
		return (NULL);
	i = 0;
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
